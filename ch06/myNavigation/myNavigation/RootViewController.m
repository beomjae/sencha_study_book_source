// nsMutableArray에 등록된 아이템의 갯수만큼 tableView에 등록
- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [nsMutableArray count];
}


// 행을 다시 그릴 필요가 있을때 테이블뷰에서 호출한다.
// NSIndexPath에는 section 컬럼과 몇번째 row 아이템인지에 대한 정보가 있다.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithFrame:CGRectZero reuseIdentifier:CellIdentifier] autorelease];
    }
    
    cell.textLabel.text = [nsMutableArray objectAtIndex:indexPath.row];
    
    return cell;
}


